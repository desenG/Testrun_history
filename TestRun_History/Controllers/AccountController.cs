        [Authorize(Roles = "Administrator")]
        public JsonResult Load_User()
        {

            try
            {
                //get all users from database order by name
                var userList = unitOfWork.AccountRepository.GetAllUser();
                //array to store instance which is view model user information type 
                List<UserInfo> userInfolist = new List<UserInfo>();
                //copy name, assign role for user information model instance
                //and add to array
                foreach (var user in userList)
                {
                    //copy name
                    UserInfo userInfo = new UserInfo() { UserName = user.UserName};
                    if(Roles.GetRolesForUser(user.UserName).Count()>0)
                    {   
                        //if user has a role
                        userInfo.Role=Roles.GetRolesForUser(user.UserName).ToList()[0];
                    }
                    //add to array
                    userInfolist.Add(userInfo);
                }

                //return json with user list with role information
                return Json(new
                {
                    data = userInfolist,
                    success = true,
                    message = "Get users successfully!"
                }, JsonRequestBehavior.AllowGet);

            }
            catch (System.Data.DataException)
            {
                //if catch a DataException, return a json with customized information
                return Json(new
                {
                    success = false,
                    message = "EntityException was caught. Please contact with administrator."
                });
            }

        }
